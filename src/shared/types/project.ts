/**
 * Project-related types for Nightshift
 */

/**
 * Project registry entry
 * All project data including local path is stored in the projects table
 */
export interface Project {
  /** Unique project identifier (e.g., proj_abc123) */
  id: string

  /** Display name for the project */
  name: string

  /** Optional description of the project's purpose */
  description: string | null

  /** Git remote URL - null for non-git directories */
  gitUrl: string | null

  /** Default branch name - null for non-git directories */
  defaultBranch: string | null

  /** Whether to auto-include CLAUDE.md in task context */
  includeClaudeMd: boolean

  /** Tags associated with this project */
  tagIds: string[]

  /** Integrations associated with this project */
  integrationIds: string[]

  /** When the project was added */
  addedAt: string

  /** Custom icon - either a Lucide icon name or a file path/URL to an image */
  icon: string | null

  /** Filesystem path to the project directory */
  path: string | null
}

/**
 * Projects registry file structure
 */
export interface ProjectsRegistry {
  projects: Project[]
}

/**
 * Claude Code ecosystem info for a project (stored in local-state.json)
 */
export interface ProjectEcosystemInfo {
  /** Whether CLAUDE.md exists */
  hasClaudeMd: boolean

  /** Last modification time of CLAUDE.md */
  claudeMdLastModified: string | null

  /** Detected skills in .claude/skills/ */
  skills: ProjectSkill[]

  /** Detected custom commands in .claude/commands/ */
  commands: string[]

  /** Whether .claudeignore exists */
  hasClaudeIgnore: boolean

  /** When this info was last scanned */
  lastScanned: string
}

/**
 * Skill info detected in a project
 */
export interface ProjectSkill {
  /** Skill name/identifier */
  name: string

  /** Path to skill directory */
  path: string

  /** Whether skill is enabled by default */
  enabled: boolean
}

/**
 * Result from scanning a directory for git repos
 */
export interface ScannedRepo {
  /** Local filesystem path */
  path: string

  /** Git remote URL (null if no remote) */
  gitUrl: string | null

  /** Repository name (from directory or remote) */
  name: string

  /** Default branch name */
  defaultBranch: string

  /** Whether this repo is already added as a project */
  alreadyAdded: boolean

  /** Warning message (e.g., no remote configured) */
  warning: string | null
}

/**
 * Create a new project with default values
 */
export function createProject(
  id: string,
  name: string,
  gitUrl: string | null = null,
  defaultBranch: string | null = null,
  options: Partial<Project> = {}
): Project {
  return {
    id,
    name,
    description: null,
    gitUrl,
    defaultBranch,
    includeClaudeMd: true,
    tagIds: [],
    integrationIds: [],
    addedAt: new Date().toISOString(),
    icon: null,
    path: null,
    ...options
  }
}

/**
 * Generate a unique project ID
 */
export function generateProjectId(): string {
  return `proj_${Date.now().toString(36)}${Math.random().toString(36).slice(2, 7)}`
}

